'''Many Values to Multiple Variables'''
a, b, c = "Orange", "Mango", "Apple"
print (a)
print (b)
print (c)


'''One Value to Multiple Variables'''
x = y = z = "Orange"
print (x)
print (y)
print (z)


'''Unpack a Collection'''
fruits = ["Apple", "Cherry", "Grapes"]
j, k, l = fruits
print (j)
print (k)
print (l)


